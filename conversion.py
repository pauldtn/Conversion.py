"""
1. Variables num1, num2, num3, and string1 are declared with their respective values.
2. The int() function is used to convert num1 into an integer, and the result is stored in the variable int_num1.
3. Similar conversions are done for num2 to a float, num3 to a string, and string1 to an integer.
4. The results are then printed for each conversion.
"""
# Declare the variables
num1 = 99.23
num2 = 23
num3 = 150
string1 = "100"

# Convert num1 into an integer
int_num1 = int(num1)

# Print the result
print("num1 as an integer:", int_num1)

# Convert num2 into a float
float_num2 = float(num2)

# Print the result
print("num2 as a float:", float_num2)

# Convert num3 into a string
str_num3 = str(num3)

# Print the result
print("num3 as a string:", str_num3)

# Convert string1 into an integer
int_string1 = int(string1)

# Print the result
print("string1 as an integer:", int_string1)
